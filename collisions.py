from math import dist 



def _collides(circle1, circle2):
    """Check if 2 circles overlap in 2D space, z_coordinate must be the same"""
    x1, y1, z1, r1 = circle1
    x2, y2, z2, r2 = circle2
    if z1 != z2:
        return False
    d = dist((x1, y1), (x2, y2))
    return d < r1 + r2


